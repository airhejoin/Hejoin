/*************    函数名：doxor                   ****************************
【功能】：异或运算
【参数】：
【返回】：
【说明】：
**************   设计：南下三五年 2011-04-02  11:28:03***********************/
static void doxor(INT8U* sourceaddr,INT8U* targetaddr,INT8U length)
{
	INT8U i;
	
	for (i = 0;i < length;i++)
	{
		sourceaddr[i] ^= targetaddr[i];
	}
}
/*************    函数名：setbit                  ****************************
【功能】：置位
【参数】：
【返回】：
【说明】：
*********************************************************************************/
static void setbit(INT8U* dataddr,INT8U pos,INT8U b0)	
{
	INT8U byte_count;
	INT8U bit_count;
	INT8U temp;

	temp = 1;
	byte_count = (pos - 1) / 8;
	bit_count = 7 - ((pos - 1) % 8);
	temp <<= bit_count;
	
	if(b0)
	{
		dataddr[byte_count] |= temp;
	}
	else
	{
		temp = ~temp;
		dataddr[byte_count] &= temp;
	}
}
/*************    函数名：getbit                  ****************************
【功能】：获取数据  
【参数】：
【返回】：
【说明】：
*********************************************************************************/
static INT8U getbit(INT8U* dataddr,INT8U pos)	
{
	INT8U byte_count;
	INT8U bit_count;
	INT8U temp;

	temp = 1;
	byte_count = (pos - 1) / 8;
	bit_count = 7 - ((pos - 1) % 8);
	temp <<= bit_count;
	if(dataddr[byte_count] & temp)
	{
		return(1);
	}
	else 
	{
		return(0);
	}
}
/*************    函数名：selectbits              ****************************
【功能】：
【参数】：
【返回】：
【说明】：
*********************************************************************************/
static void selectbits(INT8U* source,const INT8U* table,INT8U* target,INT8U count)
{
	INT8U i;
	
	for(i = 0;i < count;i++)
	{
		setbit(target,i + 1,getbit(source,table[i])); 
	}
}
/*************    函数名：shlc                    ****************************
【功能】：加密
【参数】：
【返回】：
【说明】：
*********************************************************************************/
static void shlc(INT8U* data_p)
{
	INT8U i,b0;
	
	b0 = getbit(data_p,1);
	
	for(i = 0;i < 7;i++)
	{
		data_p[i] <<= 1;
		
		if(i != 6)
		{
			setbit(&data_p[i],8,getbit(&data_p[i + 1],1));
		}
	}
	
	setbit(data_p,56,getbit(data_p,28));
	setbit(data_p,28,b0);
}
/*************    函数名：shrc                    ****************************
【功能】：解密
【参数】：
【返回】：
【说明】：
   
*********************************************************************************/
static void shrc(INT8U* data_p)
{
	INT8U b0;
	INT8S i;  //这个符号，让人查找了半天的代码
	
	b0 = getbit(data_p,56);
	
	for(i = 6;i >= 0;i--)
	{
		data_p[i] >>= 1;
		
		if(i != 0)
		{
			setbit(&data_p[i],1,getbit(&data_p[i - 1],8)); 
		}
	}
	
	setbit(data_p,1,getbit(data_p,29));
	setbit(data_p,29,b0);
}
