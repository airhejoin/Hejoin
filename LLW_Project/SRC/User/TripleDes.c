/*
*版本说明:
		  3DES（即Triple DES）是DES向AES过渡的加密算法（1999年，NIST将3-DES指定为过
		  渡的加密标准），是DES的一个更安全的变形。它以DES为基本模块，通过组合分组方
		  法设计出分组加密算法，其具体实现如下：
		  设Ek()和Dk()代表DES算法的加密和解密过程，
		  K代表DES算法使用的密钥，P代表明文，C代表密表，这样，

			3DES加密过程为：C=Ek3(Dk2(Ek1(P)))
			3DES解密过程为：P=Dk1((EK2(Dk3(C)))

			K1、K2、K3决定了算法的安全性，若三个密钥互不相同，本质上就相当于用一个长为
			168位的密钥进行加密。多年来，它在对付强力攻击时是比较安全的。若数据对安全性
			要求不那么高，K1可以等于K3。在这种情况下，密钥的有效长度为112位。

		DES算法中只用到64位密钥中的其中56位，而第8、16、24、......64位8个位并未参与
		DES运算，这一点，向我们提出了一个应用上的要求，即DES的安全性是基于除了8，16，24，
		......64位外的其余56位的组合变化256才得以保证的。因此，在实际应用中，我们应避开
		使用第8，16，24，......64位作为有效数据位，而使用其它的56位作为有效数据位，才能
		保证DES算法安全可靠地发挥作用。

		设置DES密钥时，需要保证每个字节的最后一位不参与计算，第一个字节除外
*********************************************************************************
*/
