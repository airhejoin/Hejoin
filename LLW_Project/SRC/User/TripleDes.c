static void strans(INT8U* index,INT8U* target)
{
	INT8U row,line,t,i,j,b0,b1;
	
	b0=b1+2;  /*防止编译器给出警告*/
	b1=b0+3;  /*防止编译器给出警告*/
	for(i = 0;i < 4;i++)
	{
		row = line = t = 0;
		setbit(&line,7,b0 = getbit(index,i * 12 + 1));
		setbit(&line,8,b1 = getbit(index,i * 12 + 6));
		
		for(j = 2;j < 6;j++)
		{
			setbit(&row,3 + j,getbit(index,i * 12 + j));
		}
		
		t = DesS[i * 2][line][row];
		t <<= 4;
		line = row = 0; 
		setbit(&line,7,getbit(index,i * 12 + 7));
		setbit(&line,8,getbit(index,i * 12 + 12));
		
		for(j = 2;j < 6;j++)
		{
			setbit(&row,3 + j,getbit(index,i * 12 + 6 + j));
		}
		
		t |= DesS[i * 2 + 1][line][row];
		target[i] = t;
	}
}
/*************    函数名：Des                     ****************************
【功能】：
【参数】：
【返回】：
【说明】：
		data_p   待加密数据首地址
		key_p    密钥数据首地址
		type     0 解密   1 加密
*********************************************************************************/
void Des(INT8U *data_p,INT8U* key_p,INT8U type)
{
	INT8U tempbuf[12];
	INT8U key[7];
	INT8U i;
	INT8U j;
	INT8U count;
	void (*f)(INT8U* data_p);
	
	selectbits(data_p,DesIp,tempbuf,64);
	MemoryCopy(tempbuf,data_p,8);
	selectbits(key_p,DesPc_1,key,56);

	for(i = 0;i < 16;i ++)
	{
		selectbits(data_p + 4,DesE,tempbuf,48);

		if(type ==1)//加密
		{
			f = shlc;
			count = i;
		}
		else
		{
			f = shrc;
			count = 16-i;
		}
			

		for(j = 0;j < DesRots[count];j++)
		{
			f(key);
		}
			
		selectbits(key,DesPc_2,tempbuf + 6,48);
		doxor(tempbuf,tempbuf + 6,6);
		strans(tempbuf,tempbuf + 6);
		selectbits(tempbuf + 6,DesP,tempbuf,32);
		doxor(tempbuf,data_p,4);
			
		if(i < 15)
		{
			MemoryCopy(data_p + 4,data_p,4);
			MemoryCopy(tempbuf,data_p + 4,4);
		}
	}
	
	MemoryCopy(tempbuf,data_p,4);
	selectbits(data_p,DesIp_1,tempbuf,64);
	MemoryCopy(tempbuf,data_p,8);
}
/*************    函数名：TripDes                ****************************
【功能】：三重DES加密
【参数】：
【返回】：
【说明】：
		data_p: 待加密数据首地址
		key_x : 密钥KEY0首地址
		key_y : 密钥KEY1首地址
		type  : 1 加密  其他 解密

		注意:key_x 和 key_y 一定要不一样，这样，加密密钥理论上才可以达到112位

		共有两个密钥key1和key2： 
		加密过程：用key1加密，用key2解密,再用key1加密 
		解密过程：用key1解密，用key2加密,再用key1解密 
*********************************************************************************/
void TripDes(INT8U *data_p, INT8U* key_x, INT8U *key_y, INT8U type)
{
	if(1==type)//加密
	{
		Des(data_p, key_x, 1);
		Des(data_p, key_y, 0);
		Des(data_p, key_x, 1);
	}
	else
	{
		Des(data_p, key_x, 0);
		Des(data_p, key_y, 1);
		Des(data_p, key_x, 0);
	}
}

