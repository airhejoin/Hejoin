/*************    函数名：DesEncrypt              ****************************
【功能】：DES加密算法
【参数】：
【返回】：
【说明】：
		程序中snr指向8个字节的明文，key指向8个字节的密钥，运行程序后密文保
        存在k数组中
**************   设计：南下三五年 2011-03-24  23:42:02***********************/
void DesEncrypt( unsigned char * snr, unsigned char * key ,unsigned char *seed )
{
	unsigned char i, j, m, n, * p, s[8] = { 0 }, k[8] = { 0 };
 	unsigned long L, R, C, D, a, b, * q; 
  
 	// 明文初始化
 	L = R = 0;
 	for( i = 0; i < 64; i ++ )
 	{
 		m = 7 - ( IP_1[i] - 1 ) % 8;
  		p = snr + ( IP_1[i] - 1 ) / 8;
  		q = ( i < 32 ) ? & L : & R;
  		if( * p & ( 1 << m ))
   		* q |= ( 1 << 31 - i % 32 );
 	}
	
 	// 密钥初始化
 	C = D = 0;
 	for( i = 0; i < 56; i ++ )
 	{
  		m = 7 - ( PC_1[i] - 1 ) % 8;
  		p = key + ( PC_1[i] - 1 ) / 8;
  		q = ( i < 28 ) ?  & C : & D;
  		if( * p & ( 1 << m ))
   		* q |= ( 1 << 31 - i % 28 );
 	}

 	// 生成子密钥并进行迭代
 	for( i = 0; i < 16; i ++ )
 	{
  		// 生成子密钥
  		for( j = 0; j < 8; j ++ )
   			k[j] = 0;
		
  		m = ( i == 0 || i == 1 || i == 8 || i == 15 ) ? 1 : 2;
  		a = ( m == 1 ) ? 0x80000000 : 0xC0000000;
  		b = ( C & a ) >> 28 | C;
  		C = b << m & 0xFFFFFFF0;
  		b = ( D & a ) >> 28 | D;
  		D = b << m & 0xFFFFFFF0; 
		
  		for( j = 0; j < 48; j ++ )
  		{
   			m = 31 - ( PC_2[j] - 1 ) % 28;
   			q = ( PC_2[j] - 1 < 28 ) ? & C : & D;
   			p = & k[j / 6];
   			if( * q & ( 1 << m ))
    		* p |= ( 1 << 7 - j % 6 );
  		}
		
  		// 进行E变换并和k与或
  		for( j = 0; j < 8; j ++ )
   			s[j] = 0;
  		for( j = 0; j < 48; j ++ )
  		{
   			m = 32 - E[j];
   			p = & s[j / 6];
   			if( R & ( 1 << m ))
    			* p |= ( 1 << 7 - j % 6 );
   			if( j % 6 == 5 )
    			* p ^= k[j / 6]; 
  		}
		
  		// 经过S盒转换
  		for( j = 0; j < 8; j ++ )
  		{
   			m = ( s[j] >> 2 & 0x01 ) + ( s[j] >> 7 & 0x01 ) * 2;
   			n = ( s[j] >> 3 & 0x01 ) + ( s[j] >> 4 & 0x01 ) * 2 + ( s[j] >> 5 & 0x01 ) * 4 + ( s[j] >> 6 & 0x01 ) * 8;
   			p = ( unsigned char * )( & s_box[j] );
   			s[j] = * ( p + m * 16 + n );
  		}
		
  		// 进行P变换
  		a = 0;
  		for( j = 0; j < 32; j ++ )
  		{
   			m = 3 - ( P[j] - 1 ) % 4;
   			p = & s[( P[j] - 1 ) / 4];
   			if( * p & ( 1 << m ))
    			a |= ( 1 << 31 - j );
  		}
		
  		// 迭代结果
  		b = R;
  		R = L ^ a;
  		L = b;
 	}
	
 	for( i = 0; i < 8; i ++ )
  		k[i] = 0;
	
 	for( i = 0; i < 64; i ++ )
 	{
  		m = 31 - ( IP_2[i] - 1 ) % 32;
  		q = ( IP_2[i] <= 32 ) ? & L : & R;
  		p = & k[i / 8];
  		if( * q & ( 1 << m ))
   		* p |= ( 1 << 7 - i % 8 );
 	}

	for(i=0;i<8;i++)
	{
		*seed++=k[i];
	}
}


